package IBasicoAmbienteConceitos;

/**
 * Variáveis são usadas para armazenar valores que o software utiliza durante sua execução.
 */
public class Variaveis {
    public static void main(String[] args) {

        // Declaração de variáveis
        String nome = "Linguagem Java";
        Integer idade = 25;
        Boolean fome = false;

        // Declaração de várias variáveis na mesma linha - Nesse caso o tipo é o mesmo para todas
        String nome1 = "Java", sobrenome = "SDK";

        // Variáveis que quando inicializadas não possuem valor algum
        String str;
        int valor;
        boolean resultado;
    }
}
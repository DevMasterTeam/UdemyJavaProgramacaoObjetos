package IVOrientacaoObjetos.MaisObjetos;

public class FuncoesMath {
    public static void main(String[] args) {

        // Raíz quadrada
        Math.sqrt(4);

        // Faz o arredendamento para o número mais próximo - Retorno inteiro
        Math.round(1085.3572f);

        // O módulo de um núermo
        Math.abs(-17);

        // O máximo entre dois números
        Math.max(10, 20);

        // O máximo entre dois números
        Math.min(10, 20);

        // Número randômico - [0, 1[
        // Retorna um número double positivo maior ou igual a 0 e menor que 1.
        Math.random();


        // Constantes
        System.out.println("Número PI: " + Math.PI);
        System.out.println("Número de euler: " + Math.E);

    }
}

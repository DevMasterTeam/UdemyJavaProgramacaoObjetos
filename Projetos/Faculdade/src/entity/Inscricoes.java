package entity;

public class Inscricoes {
    private int matematica = 0;
    private int informatica = 0;
    private int engenharia = 0;

    public int getMatematica() {
        return matematica;
    }

    public void setMatematica(int matematica) {
        this.matematica = matematica;
    }

    public int getInformatica() {
        return informatica;
    }

    public void setInformatica(int informatica) {
        this.informatica = informatica;
    }

    public int getEngenharia() {
        return engenharia;
    }

    public void setEngenharia(int engenharia) {
        this.engenharia = engenharia;
    }
}
